
#Para python los tipos son las denominadas clases. Estos tres ejemplos son respectivamente 

#<class 'int'>     Refiere a numeros enteros sin parte fraccionaria
print(type(2))
#<class 'float'>  para numeros con parte fraccionaria
print(type(7.0))
#<class 'str'>    para las cadenas de caracteres o simplemente texto (String:cadenas de caracateres o cuerda. Por eso es str)
print(type("hola mundo"))

